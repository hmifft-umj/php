<?php

// And (&&)
$a = true;
$b = false;
if ($a && $b) {
  print("Both conditions are true\n");
} else {
  print("At least one condition is false\n");
}

// Or (||)
$c = true;
$d = false;
if ($c || $d) {
  print("At least one condition is true\n");
} else {
  print("Both conditions are false\n");
}

?>  